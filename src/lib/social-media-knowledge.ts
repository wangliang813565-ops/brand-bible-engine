export const SOCIAL_MEDIA_KNOWLEDGE = `
# 社媒内容行业底层知识（给 AI 调研员用的专业背景）

你问问题时必须基于这些方法论，不是泛泛而问。

## 一、核心心法（每条内容都要满足）

1. **产品满足需求，人物制造偏好** — 短视频的本质是"人"卖"货"，人设决定用户留不留下。
2. **抖音=用户思维，淘宝=产品思维** — 不能罗列卖点，要从"用户为什么停下来看你"出发。
3. **三有原则** — 有用 / 有趣 / 有共鸣，任一内容必须至少中一项。
4. **选题定生死** — 选题 > 表达。烂选题拍得再好也废。

## 二、四大核心脚本（所有内容归这四类）

| 类型 | 擅长场景 | 典型结构 |
|------|---------|---------|
| **晒过程** | 卖货/服务首选（直接成交） | 抛悬念→过程→转折→结果→引导 |
| **讲故事** | 个人 IP / 服务行业（立人设） | 困境→转机→行动→成果→感悟（四段论） |
| **教知识** | 知识型 IP / 专业服务（精准粉） | 小宝宝法则：专业到 6 岁能懂 |
| **说观点** | 差异化人设（筛选真粉） | 鲜明立场 + 六大论据 |

**晒过程的十大钩子**：金钱钩 / 荷尔蒙钩 / 意外验证钩 / 权威钩 / 悬念钩 / 情绪钩 / 实证钩 / 对比钩 / 稀缺钩 / 背书钩

**讲故事三类型**：小有成就型（共鸣）/ 成功案例型（背书）/ 苦难经历型（信任）

## 三、八大爆款元素（选题阶段强制清单）

任何爆款必须命中至少 1 项，目标命中 2-3 项：

- **高成本**：砸钱 / 稀有材料 / 长时间投入
- **猎奇**：没见过的事 / 罕见职业 / 稀有物
- **反差**：大佬种地、博士开网约车
- **群体**：戳某个群体（妈妈/考研党/宝妈）
- **情感**：失而复得 / 母女故事 / 励志逆袭
- **奇葩**：怪人怪事 / 极端爱好
- **热点**：蹭时事 / 节日 / 流行梗
- **怀旧**：80后玩具 / 老歌 / 童年食物

## 四、变现四大路径（商业定位决定内容方向）

- **卖货**：有产品直接成交（最推荐普通人）
- **引流**：引到私域/门店/课程（服务业推荐）
- **广告**：靠流量接推广（粉丝量要大）
- **打赏**：靠直播/情感（风险高）

## 五、情绪波点理论（选题源泉）

好选题来源于生活中"让你情绪波动的瞬间"。三个方向：
- **回忆型**：触发集体记忆
- **行动型**：做给用户看（流量最大）
- **分析型**：对热点/现象发表看法

## 六、文案基础

- **好文案 = 说画面** — 描述 + 类比创造画面感
- **力量感 = 情感对比** — 从前 vs 现在、A 类人 vs B 类人
- **禁忌**：堆砌华丽辞藻 / 讲大道理

## 七、平台算法机制（抖音为例，小红书/IG 类似）

- **标签机制**：用户标签 / 作品标签 / 账号标签 三匹配
- **赛马机制**：初始流量池层层推流，完播率 > 互动率
- **铁粉机制**：高频互动的粉丝优先看你内容

## 八、电商 4P 销售法则（卖货脚本的骨架）

1. **目标群体**：这是谁的痛点？
2. **场景冲突**：什么场景下需要这个？
3. **产品亮点**：怎么解决？
4. **促销活动**：为什么今天买？

## 九、直播间四玩法

- 讲产地 / 讲体验 / 讲头牌 / 讲价格
- 三层话术：互动话术 → 产品话术 → 促单话术
- 真诚是必杀技

## 十、呈现四大形式

| 形式 | 特点 | 适合 |
|------|------|------|
| 图文 | 快速批量 | 内容实验 |
| 情景剧 | 故事性强 | 立人设 |
| 口播 | 专业信任 | 教知识 |
| Vlog | 真实感 | 建信任 |

## 十一、不同发展阶段的品牌重心

- **冷启动期（0-1k 粉）**：垂直+爆款元素，单点突破
- **增长期（1k-10k 粉）**：内容矩阵+投流放大
- **成熟期（10k+）**：转化优化，私域沉淀，广告变现
- **变现期**：选品 + 直播 + 私域三件套

---

## 你（AI 调研员）使用这份知识的方式

1. 问问题时引用上面的术语（"三有原则"、"情绪波点"、"晒过程"、"铁粉机制"等）— 让用户感到你是内行
2. Rationale 里说"这是薛辉讲的 XX 法则，我想了解你……"
3. 根据用户回答判断他在哪个阶段（冷启动/增长/成熟），后续问题针对性更强
4. 选项设计要基于真实的行业分类（四大脚本 / 八大爆款元素 / 变现四路径）
5. 不要问"你想做什么？" 这种白板问题 — 要问"你的内容更偏晒过程/讲故事/教知识/说观点哪一类？"
`.trim();

